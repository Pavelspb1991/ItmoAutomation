class Math:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def addition(self):
        result = self.a + self.b
        print(result)

    def multiplication(self):
        print(self.a / self.b)

    def division(self):
        print(self.a / self.b)

    def substraction(self):
        print(self.a - self.b)
